package Basics_of_Java;

public class Rev_11_Do_While_loop {

	public static void main(String[] args) {
		// This Will Perform While Loop
		int i = 0;
		while (i <= 5) {
		  System.out.println(i);
		  i++;
		}
		// This Will Perform Do While loop
	    {
			int j = 0;
			do {
			  System.out.print(j);
			  j++;
			}
			while (j < 10);
	     }
	}
}
