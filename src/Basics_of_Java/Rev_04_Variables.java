package Basics_of_Java;

public class Rev_04_Variables {

	public static void main(String[] args) {
		//NutShell
		
		String name = "Hariom";         // Ye String Likhne ka tarika hai 
		System.out.println(name);	
		
		int myNum = 5;                  // Ye Integer Likhne ka tarika hai 
		System.out.println(myNum);
		
		float myFloatNum = 5.99f;        // Ye float Likhne ka tarika hai 
		System.out.println(myFloatNum);
		
		char myLetter = 'D';             // Ye Character Likhne ka tarika hai 
		System.out.println(myLetter);
		
		boolean myBool = true;            // Ye Boolean Likhne ka tarika hai 
		System.out.println(myBool);
	}

}
